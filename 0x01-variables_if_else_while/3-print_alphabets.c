#include <stdio.h>
#include <stdlib.h>
/**
 * main - prints alphabet in lowercase then in uppercase followed by a new line
 * Return: 0 (Success)
 */
int main(void)
{
	int ch = 'a';
	int CH = 'A';

	while (ch <= 'z')
	{
		putchar(ch);
		ch++;
	}
	while (CH <= 'Z')
	{
		putchar(CH);
		CH++;
	}

	putchar('\n');
	return (0);
}
